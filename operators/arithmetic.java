//package operators;

import java.util.*;

public class arithmetic {
    public static void main(String[] args) {
        // binary operations
        int a = 10;
        int b = 5;
        System.out.println("the sum is " + (a + b));
        System.out.println("Subtraction is" + (a - b));
        // unary operator
        System.out.println((a++));
        System.out.println((++a));
    }
}
